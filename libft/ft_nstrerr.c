#include "libft.h"

int		ft_nstrerr(int retv, char ***s)
{
	size_t	i;

	i = 0;
	if (retv == -1 || retv == 0)
		if (s && *s)
		{
			while (s[0][i])
				ft_strdel(&s[0][i++]);
			ft_strdel(*s);
		}
	return (retv);
}
