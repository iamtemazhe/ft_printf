int		ft_sqrt(int n)
{
	int i;
	int nbr;

	if (n <= 0)
		return (0);
	if (n == 1)
		return (1);
	i = 2;
	nbr = n;
	while (nbr--)
	{
		if ((n % i == 0) && (n / i == i))
			return (i);
		i++;
	}
	return (0);
}
